package cn.shoesmall.pojo;

public class Orders {
	private String orderid;
	private String accountid;
	private String shoesdetailid;
	private String generictime;
	public String getOrderid() {
		return orderid;
	}
	public void setOrderid(String orderid) {
		this.orderid = orderid;
	}
	public String getAccountid() {
		return accountid;
	}
	public void setAccountid(String accountid) {
		this.accountid = accountid;
	}
	public String getShoesdetailid() {
		return shoesdetailid;
	}
	public void setShoesdetailid(String shoesdetailid) {
		this.shoesdetailid = shoesdetailid;
	}
	public String getGenerictime() {
		return generictime;
	}
	public void setGenerictime(String generictime) {
		this.generictime = generictime;
	}
}
