package cn.shoesmall.pojo;

public class Shoestype {
	private String shoestypeid;
	private String shoestypename;
	private String whether;
	public String getShoestypeid() {
		return shoestypeid;
	}
	public void setShoestypeid(String shoestypeid) {
		this.shoestypeid = shoestypeid;
	}
	public String getShoestypename() {
		return shoestypename;
	}
	public void setShoestypename(String shoestypename) {
		this.shoestypename = shoestypename;
	}
	public String getWhether() {
		return whether;
	}
	public void setWhether(String whether) {
		this.whether = whether;
	}
}
