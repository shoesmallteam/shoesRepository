package cn.shoesmall.pojo;

public class Shoes {
	public String getShoesid() {
		return shoesid;
	}
	public void setShoesid(String shoesid) {
		this.shoesid = shoesid;
	}
	public String getShoesname() {
		return shoesname;
	}
	public void setShoesname(String shoesname) {
		this.shoesname = shoesname;
	}
	public String getShoestype() {
		return shoestype;
	}
	public void setShoestype(String shoestype) {
		this.shoestype = shoestype;
	}
	public String getShoesclassifyid() {
		return shoesclassifyid;
	}
	public void setShoesclassifyid(String shoesclassifyid) {
		this.shoesclassifyid = shoesclassifyid;
	}
	private String shoesid;
	private String shoesname;
	private String shoestype;
	private String shoesclassifyid;
}
